    public void setDegree(String degree) {
        this.degree = degree;
    }

    public String getFieldofstudy() {
        return fieldofstudy;
    }

    public void setFieldofstudy(String fieldofstudy) {
        this.fieldofstudy = fieldofstudy;
    }

    public String getGrade() {
        return grade;
    }

    public void setGrade(String grade) {
        this.grade = grade;
    }

    public String getActivities() {
        return activities;
    }

    public void setActivities(String activities) {
        this.activities = activities;
    }

    public String getEd_startdate() {
        return ed_startdate;
    }

    public void setEd_startdate(String ed_startdate) {
        this.ed_startdate = ed_startdate;
    }

    public String getEd_enddate() {
        return ed_enddate;
    }

    public void setEd_enddate(String ed_enddate) {
        this.ed_enddate = ed_enddate;
    }

    public String getEd_description() {
        return ed_description;
    }

    public void setEd_description(String ed_description) {
        this.ed_description = ed_description;
    }
